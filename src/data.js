function dynamicSort(property) {
  return function(a, b) {
    if (!b[property] || !a[property]) return 1;
    else return a[property].localeCompare(b[property]);
  };
}

export const sortedData = () => {
  let sortedScholarsDataBySortingKey = [];

  Object.values(scholarsDataObj).map((centuryObject, century) => {
    const sortedCenturyArray = Object.values(centuryObject).sort(
      dynamicSort("sortingKey")
    );
    sortedScholarsDataBySortingKey = [
      ...sortedScholarsDataBySortingKey,
      {
        [century + 2]: sortedCenturyArray
      }
    ];
  });
  return sortedScholarsDataBySortingKey;
};
